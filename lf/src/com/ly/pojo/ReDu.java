package com.ly.pojo;

public class ReDu {

	private String reduId;
	private User user;
	private String reduContent;
	private String addTime;

	public String getAddTime() {
		return addTime;
	}

	public void setAddTime(String addTime) {
		this.addTime = addTime;
	}

	public String getReduId() {
		return reduId;
	}

	public void setReduId(String reduId) {
		this.reduId = reduId;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public String getReduContent() {
		return reduContent;
	}

	public void setReduContent(String reduContent) {
		this.reduContent = reduContent;
	}

}
